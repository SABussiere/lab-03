package com.example.listycitylab3;

public class City {
    // Class variables
    private String name;
    private String province;

    // Constructor
    public City(String name, String province) {
        this.name = name;
        this.province = province;
    }

    // Name Getter
    public String getName() {
        return name;
    }

    // Province Getter
    public String getProvince() {
        return province;
    }

}
